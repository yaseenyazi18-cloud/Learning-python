#Python program that initializes a variable with a value of 10 and performs the following operations:
num1=10
print("Initial value : "+str(10))
#Add 5 to the variable using +=.
num1 += 5
print("After adding by 5 : "+str(num1))

#Subtract 3 from the variable using -=.
num1 -= 3
print("After Subtract by 3 : "+str(num1))

#Multiply the variable by 2 using *=.
num1 *= 2
print("After Multiply by 2 : "+str(num1))

#Divide the variable by 4 using /=.
num1 /= 4
print("After divided by 4 : "+str(num1))

#Find the remainder when divided by 3 using %=.
num1 %= 3
print("After finding remainder when divided by 3 : "+str(num1))

#Perform floor division by 2 using //=.
num1 //= 2
print("After floor division by 2 : "+str(num1))

#Raise the variable to the power of 3 using **=.
num1 **= 3
print("After Raising power of 3 : "+str(num1))